    private void showInforSignalName(TimeSeries timeSeries) {
        jLabelIdentifier.setText(timeSeries.getIdentifier());
        jLabelAgent.setText(timeSeries.getAgent());
        jLabelFrequency.setText(Float.toString(timeSeries.getFrequency()));
        jLabelUnits.setText(timeSeries.getUnits());
        jLabelCapacity.setText(Integer.toString(timeSeries.getCapacity()));
        jLabelOrigin.setText(Long.toString(timeSeries.getOrigin()));
        ArrayList<String> seriesIsGeneratedFrom = timeSeries.getSeriesIsGeneratedFrom();
        String stringIsGeneratedFrom = "";
        for (String serie : seriesIsGeneratedFrom) {
            stringIsGeneratedFrom=stringIsGeneratedFrom.concat(serie+"\n");
        }
        stringIsGeneratedFrom.concat("\n");
        System.out.println(stringIsGeneratedFrom);
        jTextAreaSeriesIsGeneratedFrom.setText(stringIsGeneratedFrom);

        
    }
