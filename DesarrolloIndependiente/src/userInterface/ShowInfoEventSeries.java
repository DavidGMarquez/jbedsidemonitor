    private void showInforSignalName(EventSeries eventSeries) {
        jLabelIdentifier.setText(eventSeries.getIdentifier());
        jLabelAgent.setText(eventSeries.getAgent());
        jLabelUnits.setText(eventSeries.getUnits());
        jLabelOrigin.setText(Long.toString(eventSeries.getOrigin()));
        ArrayList<String> seriesIsGeneratedFrom = eventSeries.getSeriesIsGeneratedFrom();
        String stringIsGeneratedFrom = "";
        for (String serie : seriesIsGeneratedFrom) {
            stringIsGeneratedFrom=stringIsGeneratedFrom.concat(serie+"\n");
        }
        stringIsGeneratedFrom=stringIsGeneratedFrom.concat("\n");
        jTextAreaSeriesIsGeneratedFrom.setText(stringIsGeneratedFrom);

        
    }
